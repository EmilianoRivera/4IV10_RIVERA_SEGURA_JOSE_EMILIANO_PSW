public class DCita {
    private int id_dcit;
    private String nota, talla, temp, diagnostico;


    public int getId_dcit() {
        return id_dcit;
    }

    public void setId_dcit(int id_dcit) {
        this.id_dcit = id_dcit;
    }

    public String getNota() {
        return nota;
    }

    public void setNota(String nota) {
        this.nota = nota;
    }

    public String getTalla() {
        return talla;
    }

    public void setTalla(String talla) {
        this.talla = talla;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getDiagnostico() {
        return diagnostico;
    }

    public void setDiagnostico(String diagnostico) {
        this.diagnostico = diagnostico;
    }
    
    
}
